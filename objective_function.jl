function f(
    P::Union{problem, problem_extended},
    x::Real
)::Real

    return (x - P.x_star) ^ 2
end

#----------------------------------------------------------------------------------#

function ∇f(
    P::Union{problem, problem_extended},
    x::Real
)::Real

    return 2 * (x - P.x_star)
end

